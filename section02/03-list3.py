#리스트의 인덱싱, 슬라이싱

mylist = [10, 20, 30, 40, 50]
#1번째 원소
print(mylist[1])    #20
#뒤에서 1번째 원소 
print(mylist[-1])   #50
#1번째~3번째 전까지
print(mylist[1:3])
#처음부터 3번째 전까지 
print(mylist[:3]) 
#3번째 부터 끝까지
print(mylist[3:])
#모든 원소 
print(mylist[:]) 
#1번째부터 뒤에서부터 2번째 다음까지
print(mylist[1:-2])






